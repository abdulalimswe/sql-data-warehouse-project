-- ====================================================================
-- Table Check: silver.crm_cust_info
-- ====================================================================
-- Check: Nulls or Duplicates in Primary Key (customer_id)
-- Description:
--     Ensures that the primary key column 'customer_id' contains no NULLs 
--     and has no duplicate values.
-- Expected Result:
--     No records should be returned.
-- ====================================================================

-- Check for NULL customer_id
SELECT *
FROM silver.crm_cust_info
WHERE customer_id IS NULL;

-- Check for duplicate customer_id
SELECT customer_id, COUNT(*) AS duplicate_count
FROM silver.crm_cust_info
GROUP BY customer_id
HAVING COUNT(*) > 1;


SELECT 
    cst_id,
    COUNT(*) 
FROM silver.crm_cust_info
GROUP BY cst_id
HAVING COUNT(*) > 1 OR cst_id IS NULL;

-- Check for Unwanted Spaces
-- Expectation: No Results
SELECT 
    cst_key 
FROM silver.crm_cust_info
WHERE cst_key != TRIM(cst_key);

-- Data Standardization & Consistency
SELECT DISTINCT 
    cst_marital_status 
FROM silver.crm_cust_info;

-- ====================================================================
-- Checking 'silver.crm_prd_info'
-- ====================================================================
-- Check for NULLs or Duplicates in Primary Key
-- Expectation: No Results
